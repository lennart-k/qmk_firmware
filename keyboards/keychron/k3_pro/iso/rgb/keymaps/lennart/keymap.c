void raw_hid_receive(uint8_t *data, uint8_t length) {
    print("rawhid recv\n");
    if (!length) return;

    uint8_t command = data[0];
    uprintf("command: %02x\n", command);
    switch (command)
    {
    case 0x01:
        rgb_matrix_set_color_all(255, 255, 255);
        // rgb_matrix_disable();
        // rgb_matrix_set_color(46, 255, 255, 255);
        break;

    default:
        print("invalid command");
        break;
    }
}

